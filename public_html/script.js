function validateEmail(value) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(String(value || "").trim());
}

function validateContactIntakeStep(stepIndex, langKey, { setStatus = true } = {}) {
  const state = ensureContactIntakeState();
  const locale = translations[langKey];
  const intakeDom = getContactIntakeDom();
  const setError = (key) => {
    if (setStatus && intakeDom) {
      setContactIntakeStatus(intakeDom, locale, key, "error");
    }
  };

  if (stepIndex === 0) {
    if (state.selectedServices.size < 1) {
      setError("services");
      return false;
    }
    return true;
  }

  if (stepIndex === 1) {
    if (!state.projectType || !state.timeline) {
      setError("scope");
      return false;
    }
    return true;
  }

  if (stepIndex === 2) {
    if (!state.company.trim() || !state.contactName.trim() || !validateEmail(state.email)) {
      setError("team");
      return false;
    }
    return true;
  }

  if (stepIndex === 3) {
    if (state.details.trim().length < 12 || !state.consent) {
      setError("details");
      return false;
    }
    if (isContactTurnstileRequired() && !state.turnstileToken.trim()) {
      setError("captcha");
      return false;
    }
    return true;
  }

  return true;
}

function getFirstInvalidContactIntakeStep(langKey) {
  for (let stepIndex = 0; stepIndex <= 3; stepIndex += 1) {
    if (!validateContactIntakeStep(stepIndex, langKey, { setStatus: false })) {
      return stepIndex;
    }
  }
  return -1;
}

function buildContactIntakePayload(langKey) {
  const state = ensureContactIntakeState();
  const locale = translations[langKey];
  const intakeCopy = locale.contact.intake;
  const optionMap = getContactIntakeOptionMap(langKey);
  const selectedServices = Array.from(state.selectedServices)
    .map((id) => optionMap.get(id))
    .filter(Boolean)
    .map((option) => ({
      id: option.id,
      title: option.title,
      category: option.category,
    }));

  return {
    version: "1.0",
    submittedAt: new Date().toISOString(),
    locale: langKey,
    source: {
      page: currentPage,
      url: window.location.href,
      referrer: document.referrer || "",
      userAgent: window.navigator.userAgent || "",
    },
    project: {
      services: selectedServices,
      projectType: {
        value: state.projectType,
        label: intakeCopy.projectType?.[state.projectType] || "",
      },
      timeline: {
        value: state.timeline,
        label: intakeCopy.timeline?.[state.timeline] || "",
      },
      details: state.details.trim(),
    },
    contact: {
      company: state.company.trim(),
      website: state.website.trim(),
      name: state.contactName.trim(),
      email: state.email.trim(),
      phone: state.phone.trim(),
      preferredContact: {
        value: state.preferredContact,
        label: intakeCopy.contactMethod?.[state.preferredContact] || "",
      },
    },
    consent: state.consent,
    turnstileToken: state.turnstileToken.trim(),
    honeypot: state.botField.trim(),
    form_time: Math.max(0, Math.round(Number(state.formTimeMs || 0))),
  };
}

function buildContactIntakeMailto(payload, locale) {
  const summary = locale.contact.intake.summary;
  const lines = [
    `${summary.services}: ${payload.project.services.map((entry) => `${entry.title} (${entry.category})`).join(", ")}`,
    `${summary.projectType}: ${payload.project.projectType.label}`,
    `${summary.timeline}: ${payload.project.timeline.label}`,
    `${summary.company}: ${payload.contact.company}`,
    `${summary.website}: ${payload.contact.website || summary.empty}`,
    `${summary.contact}: ${payload.contact.name}`,
    `${summary.email}: ${payload.contact.email}`,
    `${summary.phone}: ${payload.contact.phone || summary.empty}`,
    `${summary.contactMethod}: ${payload.contact.preferredContact.label}`,
    "",
    `${summary.details}:`,
    payload.project.details,
  ];
  const subject = `[Etherr] ${payload.contact.company || payload.contact.name || "Project intake"}`;
  const body = lines.join("\n");
  return `mailto:info@etherr.hr?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
}

async function submitContactIntake(langKey) {
  const intakeDom = getContactIntakeDom();
  if (!intakeDom) {
    return;
  }

  const state = ensureContactIntakeState();
  const locale = translations[langKey];

  if (state.submitting) {
    return;
  }

  const invalidStep = getFirstInvalidContactIntakeStep(langKey);
  if (invalidStep !== -1) {
    state.step = invalidStep;
    renderContactIntake(langKey);
    validateContactIntakeStep(invalidStep, langKey, { setStatus: true });
    return;
  }

  updateContactIntakeElapsedTime();
  const payload = buildContactIntakePayload(langKey);

  if (payload.honeypot) {
    contactIntakeState = createDefaultContactIntakeState();
    resetContactTurnstileToken();
    renderContactIntake(langKey);
    setContactIntakeStatus(intakeDom, locale, "success", "success");
    return;
  }

  state.submitting = true;
  renderContactIntake(langKey);
  setContactIntakeStatus(intakeDom, locale, "sending", "info");

  try {
    const response = await window.fetch(CONTACT_INTAKE_ENDPOINT, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Accept: "application/json",
      },
      body: JSON.stringify(payload),
    });

    let result = null;
    try {
      result = await response.json();
    } catch (_error) {
      result = null;
    }

    if (!response.ok || (result && result.ok === false)) {
      const error = new Error(result?.error || `Request failed with ${response.status}`);
      error.apiCode = result?.errorCode || "";
      throw error;
    }

    if (result?.status === "queued") {
      const fallbackHref = buildContactIntakeMailto(payload, locale);
      contactIntakeState = createDefaultContactIntakeState();
      resetContactTurnstileToken();
      renderContactIntake(langKey);
      setContactIntakeStatus(intakeDom, locale, "queued", "warning", fallbackHref);
      return;
    }

    contactIntakeState = createDefaultContactIntakeState();
    resetContactTurnstileToken();
    renderContactIntake(langKey);
    setContactIntakeStatus(intakeDom, locale, "success", "success");
  } catch (error) {
    const fallbackHref = buildContactIntakeMailto(payload, locale);
    if (error?.apiCode === "RATE_LIMITED") {
      setContactIntakeStatus(intakeDom, locale, "rateLimit", "error");
    } else if (error?.apiCode === "TURNSTILE_REQUIRED" || error?.apiCode === "TURNSTILE_FAILED") {
      setContactIntakeStatus(intakeDom, locale, "captcha", "error");
    } else {
      setContactIntakeStatus(intakeDom, locale, "error", "error", fallbackHref);
    }
  } finally {
    if (contactIntakeState) {
      contactIntakeState.submitting = false;
      renderContactIntake(langKey);
    }
  }
}

function renderContactIntake(langKey) {
  const intakeDom = getContactIntakeDom();
  if (!intakeDom) {
    return;
  }

  const state = ensureContactIntakeState();
  const locale = translations[langKey];
  const intakeCopy = locale.contact.intake;
  const totalSteps = intakeDom.steps.length;
  const boundedStep = clamp(state.step, 0, totalSteps - 1);
  state.step = boundedStep;

  ensureContactTurnstile(intakeDom, langKey);
  renderContactIntakeServiceOptions(intakeDom, langKey);

  intakeDom.steps.forEach((stepNode, index) => {
    if (!(stepNode instanceof HTMLElement)) {
      return;
    }

    const isActive = index === boundedStep;
    stepNode.classList.toggle("is-active", isActive);
    stepNode.hidden = !isActive;
  });

  if (intakeDom.progressFill instanceof HTMLElement) {
    const progress = ((boundedStep + 1) / totalSteps) * 100;
    intakeDom.progressFill.style.width = `${progress.toFixed(2)}%`;
  }

  if (intakeDom.progressText instanceof HTMLElement) {
    intakeDom.progressText.textContent = formatTokenString(intakeCopy.progress, {
      current: boundedStep + 1,
      total: totalSteps,
    });
  }

  const fieldNodes = intakeDom.form.querySelectorAll("[data-contact-field]");
  fieldNodes.forEach((fieldNode) => {
    if (!(fieldNode instanceof HTMLInputElement || fieldNode instanceof HTMLTextAreaElement || fieldNode instanceof HTMLSelectElement)) {
      return;
    }

    const fieldName = fieldNode.dataset.contactField;
    if (!fieldName || !(fieldName in state)) {
      return;
    }

    if (fieldNode instanceof HTMLInputElement && fieldNode.type === "checkbox") {
      fieldNode.checked = Boolean(state[fieldName]);
      return;
    }

    fieldNode.value = String(state[fieldName] || "");
  });

  const projectTypeInput = intakeDom.form.querySelector(`input[name="projectType"][value="${state.projectType}"]`);
  if (projectTypeInput instanceof HTMLInputElement) {
    projectTypeInput.checked = true;
  }

  const timelineInput = intakeDom.form.querySelector(`input[name="timeline"][value="${state.timeline}"]`);
  if (timelineInput instanceof HTMLInputElement) {
    timelineInput.checked = true;
  }

  updateContactChoiceSelectionVisuals(intakeDom.form);
  renderContactIntakeSummary(intakeDom, langKey);

  if (intakeDom.backButton instanceof HTMLButtonElement) {
    intakeDom.backButton.textContent = intakeCopy.actions.back;
    intakeDom.backButton.hidden = boundedStep === 0 || state.submitting;
    intakeDom.backButton.disabled = state.submitting;
  }

  if (intakeDom.nextButton instanceof HTMLButtonElement) {
    const isReviewStep = boundedStep === totalSteps - 2;
    intakeDom.nextButton.textContent = isReviewStep ? intakeCopy.actions.review : intakeCopy.actions.next;
    intakeDom.nextButton.hidden = boundedStep >= totalSteps - 1 || state.submitting;
    intakeDom.nextButton.disabled = state.submitting;
  }

  if (intakeDom.submitButton instanceof HTMLButtonElement) {
    intakeDom.submitButton.textContent = state.submitting ? intakeCopy.actions.sending : intakeCopy.actions.submit;
    intakeDom.submitButton.hidden = boundedStep !== totalSteps - 1;
    intakeDom.submitButton.disabled = state.submitting;
  }

  if (state.status?.key) {
    setContactIntakeStatus(
      intakeDom,
      locale,
      state.status.key,
      state.status.type,
      state.status.fallbackHref
    );
  } else {
    setContactIntakeStatus(intakeDom, locale, "", "", "");
  }
}

function initContactIntake() {
  const intakeDom = getContactIntakeDom();
  if (!intakeDom) {
    return;
  }

  ensureContactIntakeState();

  if (intakeDom.form.dataset.bound !== "true") {
    intakeDom.form.dataset.bound = "true";

    intakeDom.form.addEventListener("change", (event) => {
      const target = event.target;
      if (!(target instanceof HTMLInputElement || target instanceof HTMLSelectElement)) {
        return;
      }

      const state = ensureContactIntakeState();

      if (target.matches("[data-contact-service-option]")) {
        const serviceId = target.dataset.contactServiceOption;
        if (!serviceId) {
          return;
        }

        if (target.checked) {
          state.selectedServices.add(serviceId);
        } else {
          state.selectedServices.delete(serviceId);
        }
      } else if (target.matches('input[name="projectType"]')) {
        state.projectType = target.value || "";
      } else if (target.matches('input[name="timeline"]')) {
        state.timeline = target.value || "";
      } else if (target.matches("[data-contact-field]")) {
        const fieldName = target.dataset.contactField;
        if (!fieldName || !(fieldName in state)) {
          return;
        }
        if (target.type === "checkbox") {
          state[fieldName] = target.checked;
        } else {
          state[fieldName] = target.value;
        }
      }

      state.status = { key: "", type: "", fallbackHref: "" };
      renderContactIntake(currentLang);
    });

    intakeDom.form.addEventListener("input", (event) => {
      const target = event.target;
      if (!(target instanceof HTMLInputElement || target instanceof HTMLTextAreaElement || target instanceof HTMLSelectElement)) {
        return;
      }

      if (!target.matches("[data-contact-field]")) {
        return;
      }

      const fieldName = target.dataset.contactField;
      const state = ensureContactIntakeState();
      if (!fieldName || !(fieldName in state)) {
        return;
      }

      if (target instanceof HTMLInputElement && target.type === "checkbox") {
        state[fieldName] = target.checked;
      } else {
        state[fieldName] = target.value;
      }
    });

    if (intakeDom.backButton instanceof HTMLButtonElement) {
      intakeDom.backButton.addEventListener("click", () => {
        const state = ensureContactIntakeState();
        state.step = Math.max(0, state.step - 1);
        state.status = { key: "", type: "", fallbackHref: "" };
        renderContactIntake(currentLang);
      });
    }

    if (intakeDom.nextButton instanceof HTMLButtonElement) {
      intakeDom.nextButton.addEventListener("click", () => {
        const state = ensureContactIntakeState();
        if (!validateContactIntakeStep(state.step, currentLang, { setStatus: true })) {
          renderContactIntake(currentLang);
          return;
        }

        state.step = Math.min(intakeDom.steps.length - 1, state.step + 1);
        state.status = { key: "", type: "", fallbackHref: "" };
        renderContactIntake(currentLang);
      });
    }

    intakeDom.form.addEventListener("submit", (event) => {
      event.preventDefault();
      submitContactIntake(currentLang);
    });
  }

  renderContactIntake(currentLang);
}

function randomBetween(min, max) {
  return min + Math.random() * (max - min);
}

function clamp(value, min, max) {
  return Math.min(max, Math.max(min, value));
}

function pickRandomItem(items) {
  if (!Array.isArray(items) || !items.length) {
    return "";
  }

  return items[Math.floor(Math.random() * items.length)] || "";
}

function shuffleArray(items) {
  const copy = Array.isArray(items) ? items.slice() : [];

  for (let index = copy.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    const current = copy[index];
    copy[index] = copy[swapIndex];
    copy[swapIndex] = current;
  }

  return copy;
}

function buildSquareNodeSizeBuckets(squareNodeCount) {
  const sizes = [];
  const largestCount = Math.min(2, squareNodeCount);
  const largeCount = Math.min(4, Math.max(0, squareNodeCount - sizes.length - largestCount));

  for (let index = 0; index < largestCount; index += 1) {
    sizes.push(randomBetween(18, 22));
  }

  for (let index = 0; index < largeCount; index += 1) {
    sizes.push(randomBetween(14.5, 18));
  }

  const mediumCount = Math.min(3, Math.max(0, squareNodeCount - sizes.length));
  for (let index = 0; index < mediumCount; index += 1) {
    sizes.push(randomBetween(11, 14.5));
  }

  while (sizes.length < squareNodeCount) {
    sizes.push(randomBetween(8, 11));
  }

  return shuffleArray(sizes);
}

let heroCloudFrame = null;
const heroCloudMotionState = {
  items: [],
  lastFrameMs: 0,
};

function smoothstep(edgeStart, edgeEnd, value) {
  if (edgeStart === edgeEnd) {
    return value < edgeEnd ? 0 : 1;
  }

  const t = clamp((value - edgeStart) / (edgeEnd - edgeStart), 0, 1);
  return t * t * (3 - 2 * t);
}

function sampleHeroCloudSpeedProfile(isPrimaryLayer) {
  return pickRandomItem(
    isPrimaryLayer
      ? [
          { speedMin: 0.68, speedMax: 0.92, holdMin: 9000, holdMax: 15000 },
          { speedMin: 0.94, speedMax: 1.22, holdMin: 7000, holdMax: 12000 },
          { speedMin: 1.24, speedMax: 1.54, holdMin: 6000, holdMax: 9000 },
          { speedMin: 0.56, speedMax: 0.76, holdMin: 11000, holdMax: 18000 },
        ]
      : [
          { speedMin: 0.54, speedMax: 0.76, holdMin: 10000, holdMax: 16000 },
          { speedMin: 0.76, speedMax: 1.02, holdMin: 8000, holdMax: 13000 },
          { speedMin: 1.04, speedMax: 1.28, holdMin: 6500, holdMax: 9500 },
          { speedMin: 0.46, speedMax: 0.66, holdMin: 12000, holdMax: 18000 },
        ]
  );
}

function assignHeroCloudTargets(item, timestampMs) {
  const speedProfile = sampleHeroCloudSpeedProfile(item.isPrimaryLayer);
  const fullyOpaquePass = Math.random() < (item.isPrimaryLayer ? 0.18 : 0.08);

  item.targetSpeed = randomBetween(speedProfile.speedMin, speedProfile.speedMax);
  item.nextVariationAt = timestampMs + randomBetween(speedProfile.holdMin, speedProfile.holdMax);
  item.targetPeakOpacity = fullyOpaquePass
    ? 1
    : item.isPrimaryLayer
      ? randomBetween(0.44, 0.82)
      : randomBetween(0.24, 0.56);
  item.targetLayerBOpacity = fullyOpaquePass
    ? item.isPrimaryLayer
      ? 1
      : 0.86
    : item.isPrimaryLayer
      ? randomBetween(0.66, 0.9)
      : randomBetween(0.34, 0.64);
  item.targetLayerCOpacity = item.isPrimaryLayer
    ? fullyOpaquePass
      ? 0.94
      : randomBetween(0.42, 0.76)
    : item.targetLayerCOpacity;
  item.targetFloatY = item.isPrimaryLayer
    ? randomBetween(-10, 8)
    : randomBetween(-6, 12);
  item.floatAmplitude = item.isPrimaryLayer
    ? randomBetween(3.2, 6.8)
    : randomBetween(4.8, 8.2);
  item.breatheIntensity = item.isPrimaryLayer
    ? randomBetween(0.016, 0.038)
    : randomBetween(0.02, 0.044);
  item.breatheSpeed = item.isPrimaryLayer
    ? randomBetween(0.16, 0.28)
    : randomBetween(0.12, 0.22);
}

function resetHeroCloudState(item, timestampMs, initial = false) {
  item.startX = item.isPrimaryLayer ? randomBetween(-48, -40) : randomBetween(-60, -52);
  item.resetX = item.isPrimaryLayer ? 28 : 24;
  item.fadeInStartX = item.startX + (item.isPrimaryLayer ? 10 : 12);
  item.fadeInEndX = item.startX + (item.isPrimaryLayer ? 30 : 34);
  item.fadeOutStartX = item.resetX - (item.isPrimaryLayer ? 11 : 9);
  item.fadeOutEndX = item.resetX + 2;
  item.baseY = item.isPrimaryLayer ? randomBetween(-6, 3) : randomBetween(-2, 6);
  item.targetFloatY = item.baseY;
  item.floatY = item.baseY;
  item.phase = randomBetween(0, Math.PI * 2);
  item.phaseOffset = randomBetween(0, Math.PI * 2);
  item.x = initial
    ? randomBetween(item.fadeInStartX + 3, item.resetX - (item.isPrimaryLayer ? 10 : 8))
    : item.startX;
  assignHeroCloudTargets(item, timestampMs);
  item.speed = item.targetSpeed * randomBetween(0.92, 1.04);
  item.currentPeakOpacity = item.targetPeakOpacity;
  item.currentLayerBOpacity = item.targetLayerBOpacity;
  item.currentLayerCOpacity = item.targetLayerCOpacity || 0;
  item.currentOpacity = initial ? 0 : 0;
}

function getHeroCloudVisibleOpacity(item) {
  const fadeIn = smoothstep(item.fadeInStartX, item.fadeInEndX, item.x);
  const fadeOut = 1 - smoothstep(item.fadeOutStartX, item.fadeOutEndX, item.x);
  return clamp(fadeIn * fadeOut, 0, 1) * item.currentPeakOpacity;
}

function renderHeroCloudState(item) {
  const bob = Math.sin(item.phase) * item.floatAmplitude;
  const stretch = Math.sin(item.phase * 0.78 + item.phaseOffset) * item.breatheIntensity;
  const scaleX = item.baseScaleX + stretch;
  const scaleY = item.baseScaleY - stretch * 0.52;
  const travelY = item.floatY + bob;

  item.element.style.opacity = item.currentOpacity.toFixed(3);
  item.element.style.transform = `translate3d(${item.x.toFixed(3)}%, calc(var(--hero-cloud-render-shift-y) + ${travelY.toFixed(2)}px), 0) scale(${scaleX.toFixed(4)}, ${scaleY.toFixed(4)}) rotate(var(--hero-cloud-rotation))`;
  item.element.style.setProperty(
    "--hero-cloud-primary-layer-b-current-opacity",
    item.currentLayerBOpacity.toFixed(3)
  );
  item.element.style.setProperty(
    "--hero-cloud-primary-layer-c-current-opacity",
    item.currentLayerCOpacity.toFixed(3)
  );
  item.element.style.setProperty(
    "--hero-cloud-secondary-layer-b-current-opacity",
    item.currentLayerBOpacity.toFixed(3)
  );
}

function stopHeroCloudMotion() {
  if (heroCloudFrame !== null) {
    window.cancelAnimationFrame(heroCloudFrame);
    heroCloudFrame = null;
  }

  heroCloudMotionState.items = [];
  heroCloudMotionState.lastFrameMs = 0;
}

function stepHeroCloudMotion(timestampMs) {
  if (!heroCloudMotionState.items.length) {
    heroCloudFrame = null;
    heroCloudMotionState.lastFrameMs = 0;
    return;
  }

  const previousFrameMs = heroCloudMotionState.lastFrameMs || timestampMs;
  const deltaSeconds = clamp((timestampMs - previousFrameMs) / 1000, 0, 0.05);
  heroCloudMotionState.lastFrameMs = timestampMs;

  heroCloudMotionState.items.forEach((item) => {
    if (timestampMs >= item.nextVariationAt) {
      assignHeroCloudTargets(item, timestampMs);
    }

    item.speed += (item.targetSpeed - item.speed) * Math.min(1, deltaSeconds * 0.3);
    item.floatY += (item.targetFloatY - item.floatY) * Math.min(1, deltaSeconds * 0.22);
    item.currentPeakOpacity +=
      (item.targetPeakOpacity - item.currentPeakOpacity) * Math.min(1, deltaSeconds * 0.2);
    item.currentLayerBOpacity +=
      (item.targetLayerBOpacity - item.currentLayerBOpacity) * Math.min(1, deltaSeconds * 0.22);
    item.currentLayerCOpacity +=
      ((item.targetLayerCOpacity || 0) - item.currentLayerCOpacity) *
      Math.min(1, deltaSeconds * 0.22);
    item.phase += deltaSeconds * item.breatheSpeed;
    item.x += item.speed * deltaSeconds;

    if (item.x >= item.resetX + 2) {
      resetHeroCloudState(item, timestampMs, false);
    }

    const targetOpacity = getHeroCloudVisibleOpacity(item);
    item.currentOpacity += (targetOpacity - item.currentOpacity) * Math.min(1, deltaSeconds * 0.45);
    renderHeroCloudState(item);
  });

  heroCloudFrame = window.requestAnimationFrame(stepHeroCloudMotion);
}

