function applyStaticText(langKey) {
  const locale = translations[langKey];
  const pageMeta = locale.pages?.[currentPage]?.meta || locale.meta;
  document.documentElement.lang = langKey;
  document.title = pageMeta.title;

  if (contactStatusPopup?.close instanceof HTMLButtonElement) {
    contactStatusPopup.close.setAttribute("aria-label", getContactPopupCloseLabel());
  }

  if (dom.metaDescription) {
    dom.metaDescription.setAttribute("content", pageMeta.description);
  }

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const text = getByPath(locale, node.dataset.i18n);
    if (typeof text === "string") {
      // Special handling for contact closing text with email link
      if (node.dataset.i18n === "contact.intake.closing") {
        const emailRegex = /(info@etherr\.hr)/g;
        const parts = text.split(emailRegex);
        node.innerHTML = "";
        parts.forEach((part) => {
          if (part === "info@etherr.hr") {
            const link = document.createElement("a");
            link.href = "mailto:info@etherr.hr";
            link.textContent = part;
            link.style.color = "#28a1a1";
            link.style.textDecoration = "none";
            link.style.fontWeight = "600";
            node.appendChild(link);
          } else {
            node.appendChild(document.createTextNode(part));
          }
        });
      } else {
        if (/<(?:a\s|br\s*\/?>)/i.test(text)) {
          node.innerHTML = text;
        } else {
          node.textContent = text;
        }
      }
    }
  });

  // Handle HTML content translations (for FAQ answers)
  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const html = getByPath(locale, node.dataset.i18nHtml);
    if (typeof html === "string") {
      node.innerHTML = html;
    }
  });

  if (activeProjectDemoKey && dom.projectDemoTitle instanceof HTMLElement) {
    dom.projectDemoTitle.textContent = getProjectDemoTitle(activeProjectDemoKey);
  }

  if (activeProjectDemoKey && dom.projectDemoFrame instanceof HTMLIFrameElement) {
    dom.projectDemoFrame.title = getProjectDemoFrameTitle(activeProjectDemoKey);
  }

  const year = new Date().getFullYear();
  dom.footerCopy.textContent = locale.footer.copy.replace("{year}", String(year));

  // Generate FAQ JSON-LD schema when on the FAQ page
  if (currentPage === "faq") {
    const schemaEl = document.getElementById("faqSchema");
    if (schemaEl) {
      const faqData = locale.faq;
      const entities = [];
      if (faqData && faqData.sections) {
        Object.values(faqData.sections).forEach((section) => {
          Object.keys(section).forEach((key) => {
            if (key.startsWith("q") && section[key].question && section[key].answer) {
              // Strip HTML tags from answer for schema
              const div = document.createElement("div");
              div.innerHTML = section[key].answer;
              entities.push({
                "@type": "Question",
                "name": section[key].question,
                "acceptedAnswer": {
                  "@type": "Answer",
                  "text": div.textContent || div.innerText || "",
                },
              });
            }
          });
        });
      }
      schemaEl.textContent = JSON.stringify({
        "@context": "https://schema.org",
        "@type": "FAQPage",
        "mainEntity": entities,
      });
    }
  }
}

