function persistLanguagePreference(langKey) {
  try {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, langKey);
  } catch (_error) {
    // Ignore storage access issues and continue with the cookie fallback.
  }

  try {
    const secureFlag = window.location.protocol === "https:" ? "; Secure" : "";
    document.cookie = `${LANGUAGE_COOKIE_NAME}=${encodeURIComponent(langKey)}; Max-Age=31536000; Path=/; SameSite=Lax${secureFlag}`;
  } catch (_error) {
    // Ignore cookie write issues and continue with the in-memory language.
  }
}

function getCookieValue(cookieName) {
  const cookies = String(document.cookie || "").split(";");
  for (const entry of cookies) {
    const [rawKey, ...rest] = entry.split("=");
    if (String(rawKey || "").trim() !== cookieName) {
      continue;
    }

    return decodeURIComponent(rest.join("=").trim());
  }

  return "";
}

function getStoredLanguage() {
  try {
    const cookieValue = getCookieValue(LANGUAGE_COOKIE_NAME);
    if (translations[cookieValue]) {
      return cookieValue;
    }
  } catch (_error) {
    // Ignore cookie parsing issues and continue with localStorage.
  }

  try {
    const stored = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return translations[stored] ? stored : null;
  } catch (_error) {
    return null;
  }
}

function getAutoDetectedLanguage() {
  const locales = Array.isArray(window.navigator.languages) && window.navigator.languages.length
    ? window.navigator.languages
    : [window.navigator.language || ""];

  const normalize = (value) => String(value || "").trim().replace(/_/g, "-").toLowerCase();
  const croatianRegions = new Set(["hr", "si", "ba", "rs", "me", "al", "xk", "kv"]);
  const germanRegions = new Set(["de", "at", "ch"]);
  const croatianLanguages = new Set(["hr", "sl", "bs", "sr", "sq"]);

  for (const locale of locales.map(normalize)) {
    if (!locale) {
      continue;
    }

    const [language = "", region = ""] = locale.split("-");
    if (region && croatianRegions.has(region)) {
      return "hr";
    }
    if (region && germanRegions.has(region)) {
      return "de";
    }
    if (croatianLanguages.has(language)) {
      return "hr";
    }
    if (language === "de") {
      return "de";
    }
  }

  return "en";
}

function normalizePathname(pathname) {
  const value = String(pathname || "/").trim() || "/";
  if (value === "/index.html") {
    return "/";
  }
  if (value.endsWith("/index.html")) {
    return value.slice(0, -"index.html".length);
  }
  return value;
}

function normalizePathnameForCompare(pathname) {
  const normalized = normalizePathname(pathname);
  if (normalized !== "/" && normalized.endsWith("/")) {
    return normalized.slice(0, -1);
  }
  return normalized;
}

function isSameDocumentHashNavigation(url) {
  return normalizePathnameForCompare(url.pathname) === normalizePathnameForCompare(window.location.pathname) && Boolean(url.hash);
}

function enforceCanonicalHomePath() {
  const pathname = String(window.location.pathname || "/");
  const canonicalPath = normalizePathname(pathname);
  if (canonicalPath === pathname) {
    return;
  }

  const search = String(window.location.search || "");
  const hash = String(window.location.hash || "");
  window.history.replaceState({}, "", `${canonicalPath}${search}${hash}`);
}

function isInternalNavigationLink(link) {
  if (!(link instanceof HTMLAnchorElement)) {
    return false;
  }

  const rawHref = link.getAttribute("href");
  if (!rawHref || rawHref.startsWith("#")) {
    return false;
  }

  if (link.target && link.target !== "_self") {
    return false;
  }

  if (link.hasAttribute("download")) {
    return false;
  }

  const url = new URL(link.href, window.location.href);
  if (url.origin !== APP_ORIGIN) {
    return false;
  }

  const protocol = url.protocol.toLowerCase();
  if (protocol !== "http:" && protocol !== "https:") {
    return false;
  }

  return true;
}

function scrollToHash(hash, { smooth = true } = {}) {
  if (!hash) {
    window.scrollTo({ top: 0, behavior: smooth ? "smooth" : "auto" });
    return;
  }

  const target = document.querySelector(hash);
  if (target instanceof HTMLElement) {
    target.scrollIntoView({ behavior: smooth ? "smooth" : "auto", block: "start" });
    return;
  }

  window.scrollTo({ top: 0, behavior: smooth ? "smooth" : "auto" });
}

