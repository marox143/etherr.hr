async function navigateTo(urlLike, { replace = false, smoothScroll = false } = {}) {
  const targetUrl = new URL(urlLike, window.location.href);
  const targetPath = normalizePathname(targetUrl.pathname);
  const currentPath = normalizePathname(window.location.pathname);
  const comparableTargetPath = normalizePathnameForCompare(targetPath);
  const comparableCurrentPath = normalizePathnameForCompare(currentPath);

  if (comparableTargetPath === comparableCurrentPath) {
    const nextHref = `${targetPath}${targetUrl.hash}`;
    if (replace) {
      window.history.replaceState({}, "", nextHref);
    } else if (nextHref !== `${currentPath}${window.location.hash}`) {
      window.history.pushState({}, "", nextHref);
    }
    scrollToHashWhenStable(targetUrl.hash, { smooth: smoothScroll });
    closeMenu();
    return;
  }

  if (isNavigating) {
    return;
  }

  isNavigating = true;
  clearPageScopedEffects();
  cleanupLogoMorph();

  try {
    const response = await window.fetch(targetPath, {
      headers: {
        "X-Requested-With": "EtherrSPA",
      },
    });

    if (!response.ok) {
      window.location.href = targetUrl.href;
      return;
    }

    const html = await response.text();
    const parser = new DOMParser();
    const nextDocument = parser.parseFromString(html, "text/html");
    const nextMain = nextDocument.querySelector("main");
    const currentMain = document.querySelector("main");

    if (!(nextMain instanceof HTMLElement) || !(currentMain instanceof HTMLElement)) {
      window.location.href = targetUrl.href;
      return;
    }

    document.body.dataset.page = nextDocument.body?.dataset?.page || "home";

    // Rescue chatbot widget from <main> before it gets destroyed by the swap
    var chatWidgetInMain = currentMain.querySelector("[data-etherr-ai-widget]");
    if (chatWidgetInMain) {
      chatWidgetInMain.className = "etherr-ai-widget";
      document.body.appendChild(chatWidgetInMain);
    }

    currentMain.replaceWith(document.importNode(nextMain, true));

    if (replace) {
      window.history.replaceState({}, "", `${targetPath}${targetUrl.hash}`);
    } else {
      window.history.pushState({}, "", `${targetPath}${targetUrl.hash}`);
    }

    refreshSharedShell();
    initPageContent();
    scrollToHashWhenStable(targetUrl.hash, { smooth: smoothScroll });
    closeMenu();
  } catch (_error) {
    window.location.href = targetUrl.href;
  } finally {
    isNavigating = false;
  }
}

function initEvents() {
  if (eventsInitialized) {
    return;
  }

  eventsInitialized = true;

  document.addEventListener("click", (event) => {
    const target = event.target;
    const element = target instanceof Element ? target : null;
    const menuToggle = element?.closest(".menu-toggle");
    const langButton = element?.closest(".lang-btn");
    const popupClose = element?.closest("[data-contact-popup-close]");
    const projectDemoTrigger = element?.closest("[data-project-demo-trigger]");
    const projectDemoClose = element?.closest("[data-project-demo-close]");
    const contactSelectRoot = element?.closest("[data-contact-select]");
    const navLink = element?.closest("a");

    if (menuToggle instanceof HTMLButtonElement) {
      toggleMenu();
      return;
    }

    if (langButton instanceof HTMLButtonElement && langButton.dataset.lang) {
      setLanguage(langButton.dataset.lang);
      return;
    }

    if (popupClose instanceof HTMLButtonElement) {
      dismissContactStatusPopup();
      return;
    }

    if (projectDemoTrigger instanceof HTMLButtonElement && projectDemoTrigger.dataset.projectDemoTrigger) {
      event.preventDefault();
      openProjectDemo(projectDemoTrigger.dataset.projectDemoTrigger, projectDemoTrigger);
      return;
    }

    if (projectDemoClose instanceof HTMLButtonElement) {
      event.preventDefault();
      closeProjectDemo();
      return;
    }

    if (!(contactSelectRoot instanceof HTMLElement)) {
      closePreferredContactMenu();
    }

    if (navLink instanceof HTMLAnchorElement) {
      const isHeaderNavLink = navLink.closest(".site-nav") instanceof HTMLElement;

      if (currentPage === "home" && navLink.classList.contains("nav-home-link")) {
        event.preventDefault();
        window.scrollTo({
          top: 0,
          behavior: "smooth",
        });
        closeMenu();
        return;
      }

      const targetUrl = new URL(navLink.href, window.location.href);
      if (isSameDocumentHashNavigation(targetUrl)) {
        event.preventDefault();
        navigateToSameDocumentHash(targetUrl, { smoothScroll: true });
        return;
      }

      if (isInternalNavigationLink(navLink)) {
        event.preventDefault();
        navigateTo(targetUrl.href, { smoothScroll: false });
        return;
      }

      if (isHeaderNavLink) {
        closeMenu();
      }
    }

    if (!dom.header.contains(event.target)) {
      closeMenu();
    }

  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && dom.projectDemoOverlay instanceof HTMLElement && !dom.projectDemoOverlay.hidden) {
      closeProjectDemo();
    }
  });

  window.addEventListener(
    "scroll",
    () => {
      updateHeaderState();
      updateHeaderLogoDocking();
      updateAiIconDocking();

      // Collapse any open service panel whose active button has scrolled out of view
      if (servicePanels.length) {
        const viewportTop = 0;
        const viewportBottom = window.innerHeight;
        servicePanels.forEach((panelState) => {
          if (!isLiveServicePanelState(panelState)) {
            return;
          }
          if (isMobileServiceLayout()) {
            if (panelState.activeIndex < 0) {
              return;
            }
            const activeButton = panelState.buttons[panelState.activeIndex];
            if (!(activeButton instanceof HTMLElement)) {
              return;
            }
            const rect = activeButton.getBoundingClientRect();
            const isOutOfView = rect.bottom < viewportTop || rect.top > viewportBottom;
            if (isOutOfView) {
              panelState.activeIndex = -1;
              panelState.previewIndex = -1;
              syncServicePanelButtons(panelState);
              clearMobileServicePanelDetail(panelState);
            }
          } else {
            if (panelState.activeIndex < 0) {
              return;
            }
            const activeButton = panelState.buttons[panelState.activeIndex];
            if (!(activeButton instanceof HTMLElement)) {
              return;
            }
            const rect = activeButton.getBoundingClientRect();
            const isOutOfView = rect.bottom < viewportTop || rect.top > viewportBottom;
            if (isOutOfView) {
              panelState.activeIndex = -1;
              panelState.previewIndex = -1;
              syncServicePanelButtons(panelState);
              updatePartnerPanelFromSource(panelState, { syncLayout: true });
            }
          }
        });
      }

    },
    { passive: true }
  );

  // Throttle scroll for network animation to prevent mobile crashes
  let scrollThrottleTimer = null;
  let lastScrollTime = 0;
  window.addEventListener(
    "scroll",
    () => {
      const now = Date.now();
      // Throttle to max 30fps on mobile for better performance
      if (now - lastScrollTime < 33) {
        return;
      }
      lastScrollTime = now;
      
      if (scrollThrottleTimer) {
        return;
      }
      scrollThrottleTimer = setTimeout(() => {
        scrollThrottleTimer = null;
      }, 16);
    },
    { passive: true }
  );

  window.addEventListener(
    "pointermove",
    (event) => {
      if (event.pointerType && event.pointerType !== "mouse" && event.pointerType !== "pen") {
        return;
      }

      networkState.pointerX = event.clientX;
      networkState.pointerY = event.clientY;
      if (!networkState.pointerInitialized) {
        networkState.pointerSmoothX = event.clientX;
        networkState.pointerSmoothY = event.clientY;
        networkState.pointerInitialized = true;
      }
      networkState.pointerActive = true;
      releaseServiceHoverPreview(event.target);
    },
    { passive: true }
  );

  window.addEventListener("pointerout", (event) => {
    if (!event.relatedTarget) {
      networkState.pointerActive = false;
    }
  });

  window.addEventListener("pointercancel", () => {
    networkState.pointerActive = false;
  });

  window.addEventListener("blur", () => {
    networkState.pointerActive = false;
  });

  let resizeTimer = null;
  let demoSyncTimer = null;
  window.addEventListener("resize", () => {
    // Debounce the demo overlay sync — 250ms to let iOS rotation animation finish
    window.clearTimeout(demoSyncTimer);
    demoSyncTimer = window.setTimeout(() => {
      syncProjectDemoOverlayState();
    }, 250);

    window.clearTimeout(resizeTimer);
    resizeTimer = window.setTimeout(() => {
      // Skip heavy re-init while demo overlay is open to prevent crashes
      if (!document.body.classList.contains("has-project-demo-overlay")) {
        startNetworkLayer();
        syncProjectInlineEmbeds();
        initProjectPhoneScreens();
        initProjectLaptopEmbedScreens();
        initHeroClouds();
      }
      updateHeaderLogoDocking();
      updateAiIconDocking();
      syncProjectCardLayout();
      syncServicePanelHeights();
    }, 160);
  });

  // iOS-reliable orientation change handler — fires after rotation animation completes
  window.addEventListener("orientationchange", () => {
    setTimeout(() => {
      syncProjectDemoOverlayState();
    }, 350);
  });

  document.addEventListener("visibilitychange", () => {
    if (shouldDisableNetworkLayer()) {
      window.cancelAnimationFrame(networkFrame);
      networkFrame = null;
      return;
    }

    if (document.hidden) {
      window.cancelAnimationFrame(networkFrame);
      networkFrame = null;
      networkState.lastFrameMs = 0;
      networkState.pointerActive = false;
      return;
    }

    if (!networkFrame && !prefersReducedMotionQuery.matches) {
      networkFrame = window.requestAnimationFrame(drawNetworkLayer);
    }

    if (prefersReducedMotionQuery.matches) {
      drawNetworkLayer(window.performance.now());
    }
  });

  window.addEventListener("popstate", () => {
    navigateTo(window.location.href, { replace: true, smoothScroll: false });
  });
}

function getLanguageFromCountry(countryCode) {
  const code = String(countryCode || "").trim().toUpperCase();
  const hrCountries = new Set(["HR", "BA", "RS", "ME", "XK"]);
  const deCountries = new Set(["DE", "AT", "CH"]);
  if (hrCountries.has(code)) return "hr";
  if (deCountries.has(code)) return "de";
  return "en";
}

async function detectGeoLanguage() {
  try {
    const response = await window.fetch("https://ipapi.co/country/", {
      headers: { Accept: "text/plain" },
    });
    if (!response.ok) return null;
    const country = (await response.text()).trim();
    return getLanguageFromCountry(country);
  } catch (_error) {
    return null;
  }
}

async function init() {
  // Prevent the browser's native hash scroll — we handle it ourselves
  // after dynamic content (services, contact form) has been rendered.
  if (history.scrollRestoration) {
    history.scrollRestoration = "manual";
  }
  enforceCanonicalHomePath();
  const bootLanguage =
    translations[window.ETHERR_BOOT_LANG] ? window.ETHERR_BOOT_LANG : null;
  const storedLanguage = getStoredLanguage();

  if (storedLanguage || bootLanguage) {
    // User has an explicit preference — honour it, no geo needed.
    currentLang = bootLanguage || storedLanguage;
    initPageContent();
    startNetworkLayer();
    initEvents();
    return;
  }

  // First visit: start rendering immediately with browser-locale guess,
  // then silently correct with geo result if it differs.
  const browserLang = getAutoDetectedLanguage();
  currentLang = browserLang;
  initPageContent();
  startNetworkLayer();
  initEvents();

  const geoLang = await detectGeoLanguage();
  if (geoLang && geoLang !== currentLang) {
    setLanguage(geoLang);
  } else {
    // Persist whichever language we ended up with so geo isn't called again.
    persistLanguagePreference(currentLang);
  }
}

